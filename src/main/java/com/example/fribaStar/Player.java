package com.example.fribaStar;

public class Player {


	private int hole;
	private String name;
	private int total;
	
	public int getHole() {
		return hole;
	}
	public void setHole(int hole) {
		this.hole = hole;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	
}
