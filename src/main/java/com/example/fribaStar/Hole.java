package com.example.fribaStar;

public class Hole {
	private int score;
	private int par;
	private int scorePar;
	
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public int getPar() {
		return par;
	}
	public void setPar(int par) {
		this.par = par;
	}
	public int getScorePar() {
		return scorePar;
	}
	public void setScorePar(int scorePar) {
		this.scorePar = scorePar;
	}
		
}
